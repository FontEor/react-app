import React, { useEffect, useState } from "react";
const TestPage = () => {
  const [users, setUsers] = useState([]);


  useEffect(() => {
    fetch("/api/users")
      .then((res) => res.json())
      .then((data) => setUsers(data));
  }, []);

  return (
    <ul>
      {users.map((user) => (
        <li key={user.id}>{user.name}</li>
      ))}
    </ul>
  );
};

export default TestPage;
